
// Mutable Strings
public class Mutable1 {
    public static void main(String[] args) {
        StringBuilder sb=new StringBuilder("Biswajit");//Mutable String
        sb.append("Dey");
        
        System.out.println(sb);
        StringBuffer sb1=new StringBuffer("Biswajit");//Mutable String
        sb1.append("Dey");
        
        System.out.println(sb1);


    }
    
}
