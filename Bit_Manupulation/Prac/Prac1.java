package Prac;

public class Prac1 {
    public static void main(String[] args) {
        // And Opertaor
        System.out.println("And Operation :" + (5 & 6));// 4

        // OR opoerator
        System.out.println("OR opoerator :" + (5 | 6));// 7
        // XOR operator
        System.out.println("XOR operator " + (5 ^ 6));
        // O's and 1's compliment
        System.out.println(~1);// -2
        System.out.println(~0);// -1

        // Binary Left Shift Opertor
        System.out.println(1 << 2);

        // Binary Right Shift Opertor
        System.out.println(6 >> 1);

        
    }
}
