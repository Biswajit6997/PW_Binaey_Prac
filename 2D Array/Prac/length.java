public class length {
    public static void main(String[] args) {
        int arr[]={1,343,435,45,345,456,456};
        System.out.println(arr.length);//7

        int nums[][] = {     { 1, 2, 3, 4 },
                            { 5, 8, 14, 18 }, 
                            { 9, 10, 11, 12 }
                        };
        System.out.println(nums.length);//3

        System.out.println(nums[0].length);//4

        String a="Biswajit";
        System.out.println(a.length());//8
    }
}
